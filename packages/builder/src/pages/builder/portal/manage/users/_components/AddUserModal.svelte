<script>
  import {
    Body,
    Input,
    Label,
    ModalContent,
    notifications,
    Select,
    Toggle,
  } from "@budibase/bbui"
  import { createValidationStore, emailValidator } from "helpers/validation"
  import { users } from "stores/portal"
  import { createEventDispatcher } from "svelte"

  const dispatch = createEventDispatcher()
  const password = Math.random().toString(36).substring(2, 22)
  const options = ["Email onboarding", "Basic onboarding"]
  const [email, error, touched] = createValidationStore("", emailValidator)
  let disabled
  let builder
  let admin
  let selected = "Email onboarding"

  $: basic = selected === "Basic onboarding"

  function addUser() {
    if (basic) {
      createUser()
    } else {
      createUserFlow()
    }
  }

  async function createUser() {
    try {
      await users.create({
        email: $email,
        password,
        builder,
        admin,
        forceResetPassword: true,
      })
      notifications.success("Successfully created user")
      dispatch("created")
    } catch (error) {
      notifications.error("Error creating user")
    }
  }

  async function createUserFlow() {
    try {
      const res = await users.invite({ email: $email, builder, admin })
      notifications.success(res.message)
    } catch (error) {
      notifications.error("Error inviting user")
    }
  }
</script>

<ModalContent
  onConfirm={addUser}
  size="M"
  title="Add new user"
  confirmText="Add user"
  confirmDisabled={disabled}
  cancelText="Cancel"
  disabled={$error}
  showCloseIcon={false}
>
  <Body size="S">
    If you have SMTP configured and an email for the new user, you can use the
    automated email onboarding flow. Otherwise, use our basic onboarding process
    with autogenerated passwords.
  </Body>
  <Select
    placeholder={null}
    bind:value={selected}
    {options}
    label="Add new user via:"
  />

  <Input
    type="email"
    label="Email"
    bind:value={$email}
    error={$touched && $error}
    placeholder="john@doe.com"
  />

  {#if basic}
    <Input disabled label="Password" value={password} />
  {/if}

  <div>
    <div class="toggle">
      <Label size="L">Development access</Label>
      <Toggle text="" bind:value={builder} />
    </div>
    <div class="toggle">
      <Label size="L">Administration access</Label>
      <Toggle text="" bind:value={admin} />
    </div>
  </div>
</ModalContent>

<style>
  .toggle {
    display: grid;
    grid-template-columns: 78% 1fr;
    align-items: center;
    width: 50%;
  }
</style>
