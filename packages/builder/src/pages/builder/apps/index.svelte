<script>
  import {
    Heading,
    Layout,
    Divider,
    ActionMenu,
    MenuItem,
    Avatar,
    Page,
    Icon,
    Body,
    Modal,
  } from "@budibase/bbui"
  import { onMount } from "svelte"
  import { apps, organisation, auth } from "stores/portal"
  import { goto } from "@roxi/routify"
  import { AppStatus } from "constants"
  import { gradient } from "actions"
  import UpdateUserInfoModal from "components/settings/UpdateUserInfoModal.svelte"
  import ChangePasswordModal from "components/settings/ChangePasswordModal.svelte"
  import { processStringSync } from "@budibase/string-templates"
  import Logo from "assets/bb-emblem.svg"
  import { _ as t } from "svelte-i18n"

  let loaded = false
  let userInfoModal
  let changePasswordModal

  onMount(async () => {
    await organisation.init()
    await apps.load()
    loaded = true
  })

  const publishedAppsOnly = app => app.status === AppStatus.DEPLOYED

  $: publishedApps = $apps.filter(publishedAppsOnly)
</script>

{#if $auth.user && loaded}
  <div class="container">
    <Page>
      <div class="content">
        <Layout noPadding>
          <img alt="logo" src={$organisation.logoUrl || Logo} />
          <div class="info-title">
            <Layout noPadding gap="XS">
              <Heading size="L">
                { $t('hey') } {$auth.user.firstName || $auth.user.email}
              </Heading>
              <Body>
                { $t('welcome-to-the') } {$organisation.company} { $t('portal-below-youll-find-the-list-of-apps-that-you-have-access-to') }
              </Body>
            </Layout>
            <ActionMenu align="right">
              <div slot="control" class="avatar">
                <Avatar
                  size="M"
                  initials={$auth.initials}
                  url={$auth.user.pictureUrl}
                />
                <Icon size="XL" name="ChevronDown" />
              </div>
              <MenuItem icon="UserEdit" on:click={() => userInfoModal.show()}>
                { $t('update-user-information') }
              </MenuItem>
              <MenuItem
                icon="LockClosed"
                on:click={() => changePasswordModal.show()}
              >
                { $t('update-password') }
              </MenuItem>
              {#if $auth.isBuilder}
                <MenuItem
                  icon="UserDeveloper"
                  on:click={() => $goto("../portal")}
                >
                  { $t('open-developer-mode') }
                </MenuItem>
              {/if}
              <MenuItem icon="LogOut" on:click={auth.logout}>{ $t('log-out') }</MenuItem>
            </ActionMenu>
          </div>
          <Divider />
          {#if publishedApps.length}
            <Heading>{ $t('apps') }</Heading>
            <div class="group">
              <Layout gap="S" noPadding>
                {#each publishedApps as app, idx (app.appId)}
                  <a class="app" target="_blank" href={`/${app.prodId}`}>
                    <div class="preview" use:gradient={{ seed: app.name }} />
                    <div class="app-info">
                      <Heading size="XS">{app.name}</Heading>
                      <Body size="S">
                        {#if app.updatedAt}
                          {processStringSync(
                            "Updated {{ duration time 'millisecond' }} ago",
                            {
                              time:
                                new Date().getTime() -
                                new Date(app.updatedAt).getTime(),
                            }
                          )}
                        {:else}
                          Never updated
                        {/if}
                      </Body>
                    </div>
                    <Icon name="ChevronRight" />
                  </a>
                {/each}
              </Layout>
            </div>
          {:else}
            <Layout gap="XS" noPadding>
              <Heading size="S">{ $t('you-dont-have-access-to-any-apps-yet') }</Heading>
              <Body size="S">
                { $t('the-apps-you-have-access-to-will-be-listed-here') }
              </Body>
            </Layout>
          {/if}
        </Layout>
      </div>
    </Page>
  </div>
  <Modal bind:this={userInfoModal}>
    <UpdateUserInfoModal />
  </Modal>
  <Modal bind:this={changePasswordModal}>
    <ChangePasswordModal />
  </Modal>
{/if}

