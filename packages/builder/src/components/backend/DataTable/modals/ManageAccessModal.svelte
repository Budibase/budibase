<script>
  import { roles, permissions as permissionsStore } from "stores/backend"
  import {
    Label,
    Input,
    Select,
    notifications,
    Body,
    ModalContent,
  } from "@budibase/bbui"
  import { capitalise } from "helpers"
  import { _ } from "../../../../../lang/i18n"

  export let resourceId
  export let permissions

  async function changePermission(level, role) {
    try {
      await permissionsStore.save({
        level,
        role,
        resource: resourceId,
      })

      // Show updated permissions in UI: REMOVE
      permissions = await permissionsStore.forResource(resourceId)
      notifications.success(
        $_(
          "components.backend.DataTable.modals.ManaheAccessModal.Updated_permissions"
        )
      )
    } catch (error) {
      notifications.error(
        $_(
          "components.backend.DataTable.modals.ManaheAccessModal.Error_updating"
        )
      )
    }
  }
</script>

<ModalContent
  title={$_(
    "components.backend.DataTable.modals.ManaheAccessModal.Manage_Access"
  )}
  showCancelButton={false}
  confirmText={$_("components.backend.DataTable.modals.ManaheAccessModal.Done")}
>
  <Body size="S"
    >{$_(
      "components.backend.DataTable.modals.ManaheAccessModal.Specify_data"
    )}</Body
  >
  <div class="row">
    <Label extraSmall grey
      >{$_(
        "components.backend.DataTable.modals.ManaheAccessModal.Level"
      )}</Label
    >
    <Label extraSmall grey
      >{$_("components.backend.DataTable.modals.ManaheAccessModal.Role")}</Label
    >
    {#each Object.keys(permissions) as level}
      <Input value={capitalise(level)} disabled />
      <Select
        value={permissions[level]}
        on:change={e => changePermission(level, e.detail)}
        options={$roles}
        getOptionLabel={x => x.name}
        getOptionValue={x => x._id}
      />
    {/each}
  </div>
</ModalContent>

<style>
  .row {
    display: grid;
    grid-template-columns: 1fr 1fr;
    grid-gap: var(--spacing-s);
  }
</style>
